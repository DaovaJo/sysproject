def sort_dict():
    # Имеем какой-то словарь 
    dict_1 = {"Смирнов К.С.": 3, "Поляков К.В.": 4, "Платонов Д.А." : 5, "Захаров С.С": 2}

    # Применяем сортировку, преобразовывая в кортеж 
    sort = sorted(dict_1.items(), key=lambda x: x[0])
    # Преобразовываем обратно кортеж в словарь 
    dict_1 = dict(sort)

    # выводим через запятую значения 
    print(*dict_1, sep = " , ")

sort_dict()